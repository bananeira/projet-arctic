document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll("p, pre, code, li, ul, ol, h1, h2, h3, h4, h5, h6").forEach(el => {
        el.innerHTML = el.innerHTML
            .replace(/!=/g, '≠')
            .replace(/&lt;_/g, '←')
            .replace(/_&gt;/g, '→')
            .replace(/&lt;=/g, '≤')
            .replace(/&gt;=/g, '≥')
            .replace(/ --- /g, ' — ')
            .replace(/ -- /g, '–')
            .replace(/\.\.\./g, '…')
            .replace(/\\exists\b/g, '∃')
            .replace(/\\forall\b/g, '∀')
            .replace(/\\neg\b/g, '¬')
            .replace(/\\land\b/g, '∧')
            .replace(/\\lor\b/g, '∨')
            .replace(/\\implies\b/g, '⇒')
            .replace(/\\iff\b/g, '⇔')
            .replace(/\\neq\b/g, '≠')
            .replace(/\\in\b/g, '∈')
            .replace(/\\notin\b/g, '∉')
            .replace(/\\subset\b/g, '⊂')
            .replace(/\\infty\b/g, '∞')
            .replace(/\\cup\b/g, '∪')
            .replace(/\\cap\b/g, '∩')
            .replace(/\\bigcup\b/g, '⋃')
            .replace(/\\bigcap\b/g, '⋂')
            .replace(/\\emptyset\b/g, '∅')
            .replace(/\\circ\b/g, '∘')
            .replace(/\\times\b/g, '×')
            .replace(/\\cdot\b/g, '⋅')
            .replace(/\\pm\b/g, '±')
            .replace(/\\mp\b/g, '∓')
            .replace(/\\subseteq\b/g, '⊆')
            .replace(/\\supset\b/g, '⊃')
            .replace(/\\supseteq\b/g, '⊇')
            .replace(/\\propto\b/g, '∝')
            .replace(/\\aleph\b/g, 'ℵ')
            .replace(/\\emptyset\b/g, '∅')
            .replace(/\\sqcup\b/g, '⊔')
            .replace(/\\NN\b/g, 'ℕ')
            .replace(/\\ZZ\b/g, 'ℤ')
            .replace(/\\QQ\b/g, 'ℚ')
            .replace(/\\RR\b/g, 'ℝ')
            .replace(/\\CC\b/g, 'ℂ')
            .replace(/\\ell\b/g, 'ℓ')
    });
});