document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll("p, pre, li, ul, ol, h1, h2, h3, h4, h5, h6").forEach(el => {
        el.innerHTML = el.innerHTML
            .replace(/!=/g, '≠')
            .replace(/<=/g, '≤')
            .replace(/>=/g, '≥')
            .replace(/<-/g, '←')
            .replace(/->/g, '→')
            .replace(/ --- /g, '—')
            .replace(/ -- /g, ' – ')
            .replace(/\.\.\./g, '…')
            .replace(/\\exists\b/g, '∃')
            .replace(/\\forall\b/g, '∀')
            .replace(/\\neg\b/g, '¬')
            .replace(/\\land\b/g, '∧')
            .replace(/\\lor\b/g, '∨')
            .replace(/\\implies\b/g, '⇒')
            .replace(/\\iff\b/g, '⇔')
            .replace(/\\leq\b/g, '≤')
            .replace(/\\geq\b/g, '≥')
            .replace(/\\neq\b/g, '≠')
            .replace(/\\in\b/g, '∈')
            .replace(/\\notin\b/g, '∉')
            .replace(/\\subset\b/g, '⊂')
            .replace(/\\infty\b/g, '∞')
            .replace(/\\cup\b/g, '∪')
            .replace(/\\cap\b/g, '∩')
            .replace(/\\bigcup\b/g, '⋃')
            .replace(/\\bigcap\b/g, '⋂')
            .replace(/\\emptyset\b/g, '∅')
            .replace(/\\circ\b/g, '∘')
            .replace(/\\times\b/g, '×')
            .replace(/\\div\b/g, '÷')
            .replace(/\\propto\b/g, '∝')
            .replace(/\\degree\b/g, '°')
            .replace(/\\aleph\b/g, 'ℵ')
            .replace(/\\emptyset\b/g, '∅')
            .replace(/\\sqcup\b/g, '⊔')
    });
});