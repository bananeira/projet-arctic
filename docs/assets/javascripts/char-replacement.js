document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll("p, pre, code, li, ul, ol, h1, h2, h3, h4, h5, h6").forEach(el => {
        el.innerHTML = el.innerHTML
            .replace(/!=/g, '≠')
            .replace(/&lt;_/g, '←')
            .replace(/_&gt;/g, '→')
            .replace(/&lt;=/g, '≤')
            .replace(/&gt;=/g, '≥')
            .replace(/ --- /g, ' — ')
            .replace(/ -- /g, '–')
            .replace(/\.\.\./g, '…')
            .replace(/\\epsilon/g, 'ε')
            .replace(/\\exists/g, '∃')
            .replace(/\\forall/g, '∀')
            .replace(/\\neg/g, '¬')
            .replace(/\\land/g, '∧')
            .replace(/\\lor/g, '∨')
            .replace(/\\implies/g, '⇒')
            .replace(/\\iff/g, '⇔')
            .replace(/\\neq/g, '≠')
            .replace(/\\notin/g, '∉')
            .replace(/\\infty/g, '∞')
            .replace(/\\in/g, '∈')
            .replace(/\\cup/g, '∪')
            .replace(/\\cap/g, '∩')
            .replace(/\\bigcup/g, '⋃')
            .replace(/\\bigcap/g, '⋂')
            .replace(/\\emptyset/g, '∅')
            .replace(/\\circ/g, '∘')
            .replace(/\\times/g, '×')
            .replace(/\\cdot/g, '⋅')
            .replace(/\\pm/g, '±')
            .replace(/\\mp/g, '∓')
            .replace(/\\supseteq/g, '⊇')
            .replace(/\\supset/g, '⊃')
            .replace(/\\subseteq/g, '⊆')
            .replace(/\\subset/g, '⊂')
            .replace(/\\propto/g, '∝')
            .replace(/\\aleph/g, 'ℵ')
            .replace(/\\emptyset/g, '∅')
            .replace(/\\sqcup/g, '⊔')
            .replace(/\\NN/g, 'ℕ')
            .replace(/\\ZZ/g, 'ℤ')
            .replace(/\\QQ/g, 'ℚ')
            .replace(/\\RR/g, 'ℝ')
            .replace(/\\CC/g, 'ℂ')
            .replace(/\\ell/g, 'ℓ')
    });
});